//   There are five types of operators in JavaScript :
//   1. Arithmetic Oper  2. Assignment Oper  3. Comparison Oper  4. Logical Oper  5. Other Oper.
//
//
//   1. Arithmetic Operator :
//                          Arthmetic Operator are used to perform arithmetic calculations.
