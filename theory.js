//   3. Comparison Operator :
//                           Comparison operator are used to compare two values and it return
//                           a boolean value, either true or false.

//     Exa : ( " == "  .  if the operands are equal. ) ( operands mean value. )
//           ( " === " .  if the operands and their datatype both are equal. )

//           ( " != "  .  if the operands are not equal.)
//           ( " !== " .  if the operands and their datatype both are not equal.)
//
//           ( " > "   .  if left operands is greater than the right operand.)
//           ( " >= "  .  if left operands is greater than or equal to the right operand.)
//
//           ( " < "   .  if right operands is greater than the left operand.)
//           ( " <= "  .  if right operands is greater than or equal to the left operand.)
//
//
//   4. Logical Operators :
//                       Logical operators are used to determine logic b/w variables or values.
